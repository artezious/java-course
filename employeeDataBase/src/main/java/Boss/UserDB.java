public class UserDB {
    private String empName;
    private String empSirName;
    private String empPosition;
    private String empSalary;
    private String empBoss;

    public String getEmpName() {
        return empName;
    }

    public void setEmpName(String empName) {
        this.empName = empName;
    }

    public String getEmpSirName() {
        return empSirName;
    }

    public void setEmpSirName(String empSirName) {
        this.empSirName = empSirName;
    }

    public String getEmpPosition() {
        return empPosition;
    }

    public void setEmpPosition(String empPosition) {
        this.empPosition = empPosition;
    }

    public String getEmpSalary() {
        return empSalary;
    }

    public void setEmpSalary(String empSalary) {
        this.empSalary = empSalary;
    }

    public String getEmpBoss() {
        return empBoss;
    }

    public void setEmpBoss(String empBoss) {
        this.empBoss = empBoss;
    }
}